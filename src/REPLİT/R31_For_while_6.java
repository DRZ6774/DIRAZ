package REPLİT;

public class R31_For_while_6 {

    /*Konsolda Alfabeyi Yazdıran programi yaziniz.

OUTPUT :

a b c .. .. .. .. y z*/

    public static void main(String[] args) {

        for (char i = 'A'; i <='Z'; i++) {
            System.out.print(i+ " ");

        }
        System.out.println();

        for (char i = 'a'; i <='z'; i++) {
            System.out.print(i+ " ");

        }
    }
}
